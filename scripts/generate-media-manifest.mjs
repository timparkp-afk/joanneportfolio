/**
 * Reads media filenames from public/ at build time and writes a small TS module.
 * Avoids fs.readdirSync in App Router server bundles (fixes Vercel 250MB function limit).
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const publicDir = path.join(root, "public");

function encodePublicAsset(parts) {
  return "/" + parts.map((part) => encodeURIComponent(part)).join("/");
}

const mediaExtPattern = /\.(mp4|webm|ogg|png|jpe?g|gif|webp)$/i;

function getChannelItems(folder) {
  const absoluteFolder = path.join(publicDir, "images", "channels", folder);
  if (!fs.existsSync(absoluteFolder)) return [];
  return fs
    .readdirSync(absoluteFolder)
    .filter((file) => mediaExtPattern.test(file))
    .sort((a, b) => a.localeCompare(b))
    .map((file) => encodePublicAsset(["images", "channels", folder, file]));
}

const channelFolders = [
  "out of home",
  "video",
  "paid media",
  "social",
  "email",
  "events",
];

const channelMediaByFolder = {};
for (const folder of channelFolders) {
  channelMediaByFolder[folder] = getChannelItems(folder);
}

const productLaunchesDir = path.join(
  publicDir,
  "images",
  "clove",
  "product launches"
);

const cloveProductLaunchPhotos = fs
  .readdirSync(productLaunchesDir)
  .filter((file) => /\.(png|jpe?g|webp|gif)$/i.test(file))
  .sort((a, b) => a.localeCompare(b))
  .map(
    (file) =>
      `/images/clove/${encodeURIComponent("product launches")}/${encodeURIComponent(file)}`
  );

const encodedCarouselFolder = `/images/${encodeURIComponent("clove")}/${encodeURIComponent(
  "product launches"
)}/${encodeURIComponent("carousel in product launches")}`;

const eventsDir = path.join(
  productLaunchesDir,
  "carousel in product launches",
  "events"
);

const cloveProductLaunchColumnOne = fs
  .readdirSync(eventsDir)
  .filter((file) => /\.(png|jpe?g|webp|gif|mp4|webm|ogg)$/i.test(file))
  .sort((a, b) => a.localeCompare(b))
  .map(
    (file) =>
      `${encodedCarouselFolder}/${encodeURIComponent("events")}/${encodeURIComponent(file)}`
  );

const banner = `// Generated by scripts/generate-media-manifest.mjs — do not edit by hand.
// Run: node scripts/generate-media-manifest.mjs

`;

const body = `${banner}export const channelMediaByFolder: Record<string, string[]> = ${JSON.stringify(
  channelMediaByFolder,
  null,
  2
)};

export const cloveProductLaunchPhotos: string[] = ${JSON.stringify(
  cloveProductLaunchPhotos,
  null,
  2
)};

export const cloveProductLaunchColumnOne: string[] = ${JSON.stringify(
  cloveProductLaunchColumnOne,
  null,
  2
)};
`;

const outPath = path.join(root, "app", "generated", "media-manifest.ts");
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, body, "utf8");
console.log("Wrote", path.relative(root, outPath));
